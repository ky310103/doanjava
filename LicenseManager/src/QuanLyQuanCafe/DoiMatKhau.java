    private void btnThayDoiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThayDoiMouseClicked
        if (txtTK.getText().equals("") || 
            txtMatKhau.getText().equals("") || 
            txtConfirmMatKhau.getText().equals("") ) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin");
        } else {
            String sql = "update TAIKHOAN set username = ?, pass = ? where id_NV =?";
            try {
                pstmt = conn.prepareStatement(sql);
                pstmt.setString(1, txtTK.getText());
                pstmt.setString(2, txtMatKhau.getText());
                pstmt.setInt(3, loginForm.curID_NV);
                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Thay đổi tài khoản thành công!");
            } catch (SQLException e) {
                System.out.println(e.getMessage());
                JOptionPane.showMessageDialog(null, "Thay đổi tài khoản không thành công!");
            }
            txtTK.setText(null);
            txtMatKhau.setText(null); 
            txtConfirmMatKhau.setText(null);
        }
    }//GEN-LAST:event_btnThayDoiMouseClicked
